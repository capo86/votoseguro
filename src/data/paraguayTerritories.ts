export const PARAGUAY_DEPARTMENTS = PARAGUAY_TERRITORIES.map(({ code, name }) => ({ code, name }));

export function getParaguayCitiesByDepartment(departmentName: string) {
  const territory = PARAGUAY_TERRITORIES.find((item) => item.name === departmentName);

  return territory?.cities ?? [];
}

function normalizeTerritoryName(value?: string) {
  return value
    ?.normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, " ")
    .trim()
    .toUpperCase();
}

export function findParaguayDepartmentName(value?: string) {
  const normalizedValue = normalizeTerritoryName(value);

  return PARAGUAY_DEPARTMENTS.find(
    (department) => normalizeTerritoryName(department.name) === normalizedValue,
  )?.name;
}

export function findParaguayCityName(departmentName: string, value?: string) {
  const normalizedValue = normalizeTerritoryName(value);

  return getParaguayCitiesByDepartment(departmentName).find(
    (city) => normalizeTerritoryName(city) === normalizedValue,
  );
}
